use geo;
use geo::{ Dir, Pos };
use geo::Dir::*;

use std::num::FromPrimitive;

#[derive(Copy, Clone)]
pub struct Tile {
    pub kind: TileKind
}

impl Tile {
    pub fn is_walkable(&self) -> bool {
        match self.kind {
            TileKind::Wall | TileKind::DoorClosed => false,
            _ => true
        }
    }

    pub fn is_translucent(&self) -> bool {
        self.is_walkable()
    }
}

#[derive(PartialEq, Show, FromPrimitive, Clone, Copy)]
pub enum TileKind {
    Floor       = ' ' as isize,
    Wall        = '#' as isize,
    DoorClosed  = '+' as isize,
    DoorOpen    = ',' as isize,
    StairsUp    = '<' as isize,
    StairsDown  = '>' as isize
}

impl TileKind {
    fn from_char(stile: char) -> TileKind {
        match FromPrimitive::from_int(stile as isize) {
            Some(tile) => tile,
            None       => TileKind::Floor
        }
    }

    pub fn to_char(&self) -> char {
        (*self).clone() as isize as u8 as char
    }
}

