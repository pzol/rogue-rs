#[deriving(Copy, Clone)]
pub struct Pos {
    pub x: uint,
    pub y: uint
}

#[deriving(Copy, Clone)]
pub struct Tile {
    pub kind: TileKind
}

impl Tile {
    pub fn is_walkable(&self) -> bool {
        match self.kind {
            TileKind::Wall | TileKind::DoorClosed => false,
            _ => true
        }
    }
}

#[deriving(PartialEq, Show, FromPrimitive, Clone, Copy)]
pub enum TileKind {
    Floor       = ' ' as int,
    Wall        = '#' as int,
    DoorClosed  = '+' as int,
    DoorOpen    = ',' as int,
    StairsUp    = '<' as int,
    StairsDown  = '>' as int
}

impl TileKind {
    fn from_char(stile: char) -> TileKind {
        match FromPrimitive::from_int(stile as int) {
            Some(tile) => tile,
            None       => TileKind::Floor
        }
    }

    pub fn to_char(&self) -> char {
        (*self).clone() as int as u8 as char
    }
}

